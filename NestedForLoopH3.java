        
import java.io.*;
 class NestedForLoopH3{
     public static void main(String []args){
      
      int z=1;
    for(int i=1; i<=3; i++)
         {
       for(int j=3; j>=i; j--)
              {
          System.out.print(z+" ");
             z++;
              }
          System.out.print("\n");
         }
}
}



       